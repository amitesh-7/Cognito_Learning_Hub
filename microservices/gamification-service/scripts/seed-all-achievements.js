async function seedAllAchievements() {
  try {
    console.log('ğŸ”— Connecting to MongoDB...');
    const mongoUri = process.env.MONGODB_URI || process.env.MONGO_URI;
    
    if (!mongoUri) {
      throw new Error('MongoDB URI not found in environment variables');
    }

    await mongoose.connect(mongoUri);
    console.log('âœ… Connected to MongoDB');

    console.log(`\nğŸŒ± Seeding ${allAchievements.length} achievements...\n`);
    
    let createdCount = 0;
    let updatedCount = 0;

    for (const achievementData of allAchievements) {
      const existing = await Achievement.findOne({ name: achievementData.name });
      
      if (existing) {
        // Update existing achievement
        await Achievement.updateOne(
          { _id: existing._id },
          { $set: achievementData }
        );
        updatedCount++;
        console.log(`  â†» Updated: ${achievementData.name} (${achievementData.type})`);
      } else {
        // Create new achievement
        await Achievement.create(achievementData);
        createdCount++;
        console.log(`  âœ“ Created: ${achievementData.name} (${achievementData.type})`);
      }
    }

    // Get counts by type
    const typeCounts = await Achievement.aggregate([
      { $group: { _id: '$type', count: { $sum: 1 } } },
      { $sort: { count: -1 } }
    ]);

    console.log('\nğŸ“Š Achievement Distribution by Type:');
    typeCounts.forEach(({ _id, count }) => {
      console.log(`  - ${_id}: ${count} achievements`);
    });

    console.log('\nğŸ“ˆ Summary:');
    console.log(`  - Created: ${createdCount}`);
    console.log(`  - Updated: ${updatedCount}`);
    console.log(`  - Total in DB: ${await Achievement.countDocuments()}`);
    console.log('\nâœ… All 125 achievements seeded successfully!');
    console.log('ğŸ® Refresh your frontend to see all achievements!\n');

  } catch (error) {
    console.error('âŒ Error seeding achievements:', error);
    process.exit(1);
  } finally {
    await mongoose.disconnect();
    console.log('ğŸ‘‹ Disconnected from MongoDB');
  }
}

// Run the seed function
seedAllAchievements();
